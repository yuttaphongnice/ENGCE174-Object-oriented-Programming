# List initinlization
numbers = [1, 2, 3, 4, 5 ]

# Accessing elements
print("First element:" , numbers[0])
print("Last element:" , numbers[-1])

# Slicing
print("Sliced element:" , numbers[2:4])

# Appending and extending
numbers.append(6)
print("After append:" , numbers)

# Removing elements
numbers.remove(3)
print("After removal:" , numbers)